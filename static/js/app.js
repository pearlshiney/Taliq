// Variables for MediaRecorder
let mediaRecorder = null;      // The MediaRecorder instance
let audioChunks = [];          // Array to store audio data chunks
let stream = null;             // The MediaStream from getUserMedia
let recordingStartTime = null; // Timestamp when recording started
let recordingDuration = 0;     // Duration of recording in seconds

/**
 * startRecording()
 * 
 * Initializes the MediaRecorder and starts recording audio from the microphone.
 * Requests microphone permission from the user.
 */
async function startRecording() {
    try {
        // Request microphone access from the browser
        // This will show a permission prompt to the user
        stream = await navigator.mediaDevices.getUserMedia({ audio: true });
        
        // Create a MediaRecorder instance
        // We use audio/webm as it's widely supported
        mediaRecorder = new MediaRecorder(stream, { mimeType: 'audio/webm' });
        
        // Reset audio chunks array
        audioChunks = [];
        
        // Event: Called when data is available from the recorder
        mediaRecorder.ondataavailable = (event) => {
            if (event.data.size > 0) {
                audioChunks.push(event.data);
            }
        };
        
        // Event: Called when recording stops
        mediaRecorder.onstop = () => {
            // Calculate recording duration
            recordingDuration = (Date.now() - recordingStartTime) / 1000;
            appState.recordingDuration = recordingDuration;
            
            // Combine all audio chunks into a single Blob
            const audioBlob = new Blob(audioChunks, { type: 'audio/webm' });
            appState.audioBlob = audioBlob;
            
            // Create a URL for the audio blob (for playback)
            const audioUrl = URL.createObjectURL(audioBlob);
            appState.audioUrl = audioUrl;
            
            // Update the audio preview element
            const audioPreview = document.getElementById('audio-preview');
            audioPreview.src = audioUrl;
            
            // Show the audio preview container
            document.getElementById('audio-preview-container').style.display = 'block';
            
            // Enable the submit button
            document.getElementById('submit-evaluation-btn').disabled = false;
            
            // Stop all tracks in the stream to release the microphone
            stream.getTracks().forEach(track => track.stop());
            
            console.log(`Recording duration: ${recordingDuration.toFixed(1)} seconds`);
        };
        
        // Start recording
        recordingStartTime = Date.now();
        mediaRecorder.start();
        
        // UI: Update buttons visibility
        document.getElementById('start-record-btn').style.display = 'none';
        document.getElementById('stop-record-btn').style.display = 'inline-flex';
        document.getElementById('recording-status').style.display = 'flex';
        
        // Hide any previous errors
        hideError('record-error');
        
    } catch (error) {
        // Handle permission denied or other errors
        console.error('Error starting recording:', error);
        showError('record-error', 'لا يمكن الوصول للميكروفون. يرجى السماح بالوصول والمحاولة مرة أخرى.');
    }
}

/**
 * stopRecording()
 * 
 * Stops the MediaRecorder and finalizes the audio recording.
 */
function stopRecording() {
    if (mediaRecorder && mediaRecorder.state !== 'inactive') {
        mediaRecorder.stop();
        
        // UI: Update buttons visibility
        document.getElementById('start-record-btn').style.display = 'inline-flex';
        document.getElementById('stop-record-btn').style.display = 'none';
        document.getElementById('recording-status').style.display = 'none';
    }
}

/**
 * submitForEvaluation()
 * 
 * Uploads the recorded audio to the backend for transcription.
 * After receiving the transcription, navigates to View 3.
 */
async function submitForEvaluation() {
    // Check if we have audio to submit
    if (!appState.audioBlob) {
        showError('record-error', 'يرجى تسجيل الصوت أولاً');
        return;
    }
    
    // UI: Show loading state
    const submitBtn = document.getElementById('submit-evaluation-btn');
    const btnText = submitBtn.querySelector('.btn-text');
    const btnLoading = submitBtn.querySelector('.btn-loading');
    
    submitBtn.disabled = true;
    btnText.style.display = 'none';
    btnLoading.style.display = 'inline';
    
    hideError('record-error');
    
    try {
        // Step 1: Transcribe audio
        const audioFile = new File([appState.audioBlob], 'recording.webm', {
            type: 'audio/webm'
        });
        
        const formData = new FormData();
        formData.append('audio_file', audioFile);
        formData.append('recording_duration', appState.recordingDuration.toString());
        
        const transcribeResponse = await fetch('/api/transcribe', {
            method: 'POST',
            body: formData
        });
        
        const transcribeData = await transcribeResponse.json();
        
        if (!transcribeResponse.ok) {
            throw new Error(transcribeData.error || 'فشل في تحويل الصوت لنص');
        }
        
        appState.transcribedText = transcribeData.transcription;
        appState.recordingUrl = transcribeData.recording_url || null;
        
        // Step 2: Evaluate reading performance
        const evalFormData = new FormData();
        evalFormData.append('original_text', appState.generatedText);
        evalFormData.append('transcribed_text', appState.transcribedText);
        evalFormData.append('recording_duration', appState.recordingDuration.toString());
        evalFormData.append('student_name', appState.studentName || 'غير معروف');
        evalFormData.append('difficulty', appState.difficulty || '');
        evalFormData.append('length', appState.length || '');
        evalFormData.append('recording_url', appState.recordingUrl || '');
        
        const evalResponse = await fetch('/api/evaluate', {
            method: 'POST',
            body: evalFormData
        });
        
        const evalData = await evalResponse.json();
        
        if (!evalResponse.ok) {
            throw new Error(evalData.error || 'فشل في تقييم القراءة');
        }
        
        appState.evaluation = evalData.evaluation;
        appState.evaluationId = evalData.evaluation_id || null;
        
        // Navigate to evaluation view
        showEvaluationView();
        
    } catch (error) {
        showError('record-error', error.message);
        console.error('Error in evaluation:', error);
        
    } finally {
        // UI: Restore button state
        submitBtn.disabled = false;
        btnText.style.display = 'inline';
        btnLoading.style.display = 'none';
    }
}

