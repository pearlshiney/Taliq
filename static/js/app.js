/**
 * showView(viewNumber)
 * 
 * Switches between the three main views of the application.
 * Hides all views and shows only the requested one.
 * Updates the progress steps indicator.
 * 
 * @param {number} viewNumber - The view to show (1, 2, or 3)
 */
function showView(viewNumber) {
    // Validate the view number
    if (viewNumber < 1 || viewNumber > 3) {
        console.error(`Invalid view number: ${viewNumber}`);
        return;
    }
    
    // Update state
    appState.currentView = viewNumber;
    
    // Hide all views first
    document.querySelectorAll('.view').forEach(view => {
        view.classList.remove('active');
    });
    
    // Show the requested view
    const targetView = document.getElementById(`view-${viewNumber}`);
    if (targetView) {
        targetView.classList.add('active');
    }
    
    // Update progress steps
    updateProgressSteps(viewNumber);
    
    // Scroll to top of page
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

/**
 * updateProgressSteps(currentStep)
 * 
 * Updates the visual progress indicator at the top of the page.
 * Marks the current step as active and previous steps as completed.
 * 
 * @param {number} currentStep - The current active step (1, 2, or 3)
 */
function updateProgressSteps(currentStep) {
    document.querySelectorAll('.step').forEach(step => {
        const stepNum = parseInt(step.dataset.step);
        step.classList.remove('active');
        
        if (stepNum === currentStep) {
            step.classList.add('active');
        }
    });
}

