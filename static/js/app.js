/**
 * calculateScore(original, transcribed)
 * 
 * Calculates a similarity score between the original and transcribed text.
 * Uses a simple word-matching algorithm with normalization.
 * 
 * @param {string} original - The original Arabic text
 * @param {string} transcribed - The transcribed text from ASR
 * @returns {number} - Score percentage (0-100)
 */
function calculateScore(original, transcribed) {
    // Normalize both texts: remove punctuation, extra spaces, convert to lowercase
    const normalizeText = (text) => {
        return text
            .replace(/[.,!?؛،:;"'()«»]/g, '')  // Remove punctuation
            .replace(/\s+/g, ' ')                // Collapse multiple spaces
            .trim()                               // Remove leading/trailing spaces
            .toLowerCase();                       // Case insensitive
    };
    
    const normalizedOriginal = normalizeText(original);
    const normalizedTranscribed = normalizeText(transcribed);
    
    // Split into words
    const originalWords = normalizedOriginal.split(' ').filter(w => w.length > 0);
    const transcribedWords = normalizedTranscribed.split(' ').filter(w => w.length > 0);
    
    if (originalWords.length === 0) {
        return 0;
    }
    
    // Count matching words
    let matchCount = 0;
    const maxLength = Math.max(originalWords.length, transcribedWords.length);
    
    // Simple word-by-word comparison
    for (let i = 0; i < Math.min(originalWords.length, transcribedWords.length); i++) {
        if (originalWords[i] === transcribedWords[i]) {
            matchCount++;
        }
    }
    
    // Calculate percentage based on original word count
    // We use original as reference since that's what user should have read
    const score = Math.round((matchCount / originalWords.length) * 100);
    
    return Math.min(score, 100); // Cap at 100%
}

/**
 * displayScore(score)
 * 
 * Updates the score display with appropriate styling based on the score.
 * 
 * @param {number} score - The score percentage (0-100)
 */
function displayScore(score) {
    const scoreValue = document.getElementById('score-value');
    const scoreCircle = document.querySelector('.score-circle');
    
    // Animate the score counting up
    animateScore(scoreValue, 0, score, 1000);
    
    // Apply color coding based on score
    scoreCircle.classList.remove('low-score', 'medium-score');
    
    if (score < 50) {
        scoreCircle.classList.add('low-score');
    } else if (score < 80) {
        scoreCircle.classList.add('medium-score');
    }
    // 80+ keeps the default green color
}

/**
 * animateScore(element, start, end, duration)
 * 
 * Animates the score number counting up from start to end.
 * 
 * @param {HTMLElement} element - The element to update
 * @param {number} start - Starting number
 * @param {number} end - Ending number
 * @param {number} duration - Animation duration in milliseconds
 */
function animateScore(element, start, end, duration) {
    const startTime = performance.now();
    
    function update(currentTime) {
        const elapsed = currentTime - startTime;
        const progress = Math.min(elapsed / duration, 1);
        
        // Easing function for smooth animation
        const easeOutQuart = 1 - Math.pow(1 - progress, 4);
        
        const current = Math.round(start + (end - start) * easeOutQuart);
        element.textContent = current + '%';
        
        if (progress < 1) {
            requestAnimationFrame(update);
        }
    }
    
    requestAnimationFrame(update);
}

/**
 * analyzeMistakes(original, transcribed)
 * 
 * Analyzes differences between original and transcribed text
 * and displays a summary of mistakes.
 * 
 * @param {string} original - Original text
 * @param {string} transcribed - Transcribed text
 */
function analyzeMistakes(original, transcribed) {
    const mistakesContainer = document.getElementById('mistakes-container');
    const mistakesList = document.getElementById('mistakes-list');
    
    // Clear previous mistakes
    mistakesList.innerHTML = '';
    
    // Normalize for comparison
    const normalizeText = (text) => {
        return text
            .replace(/[.,!?؛،:;"'()«»]/g, '')
            .replace(/\s+/g, ' ')
            .trim()
            .toLowerCase();
    };
    
    const originalWords = normalizeText(original).split(' ').filter(w => w.length > 0);
    const transcribedWords = normalizeText(transcribed).split(' ').filter(w => w.length > 0);
    
    const mistakes = [];
    
    // Find mismatched words
    const maxLength = Math.max(originalWords.length, transcribedWords.length);
    for (let i = 0; i < maxLength; i++) {
        const orig = originalWords[i] || '(غير موجود)';
        const trans = transcribedWords[i] || '(غير موجود)';
        
        if (orig !== trans) {
            if (originalWords[i] && !transcribedWords[i]) {
                mistakes.push(`كلمة "${originalWords[i]}" لم يتم نطقها`);
            } else if (!originalWords[i] && transcribedWords[i]) {
                mistakes.push(`كلمة إضافية "${transcribedWords[i]}" تمت إضافتها`);
            } else {
                mistakes.push(`"${originalWords[i]}" تم نطقها "${transcribedWords[i]}"`);
            }
        }
    }
    
    // Display mistakes if any
    if (mistakes.length > 0) {
        mistakes.forEach(mistake => {
            const li = document.createElement('li');
            li.textContent = mistake;
            mistakesList.appendChild(li);
        });
        mistakesContainer.style.display = 'block';
    } else {
        // No mistakes - perfect reading!
        const li = document.createElement('li');
        li.textContent = '🎉 قراءة ممتازة! لا يوجد أخطاء.';
        mistakesList.appendChild(li);
        mistakesContainer.style.display = 'block';
    }
}

/**
 * playCorrectSpeech()
 * 
 * Calls the backend to generate TTS audio for the original text,
 * then plays the audio for the user to hear correct pronunciation.
 */
async function playCorrectSpeech() {
    const playBtn = document.getElementById('play-tts-btn');
    const btnText = playBtn.querySelector('.btn-text');
    const btnLoading = playBtn.querySelector('.btn-loading');
    const ttsAudio = document.getElementById('tts-audio');
    
    // UI: Show loading
    playBtn.disabled = true;
    btnText.style.display = 'none';
    btnLoading.style.display = 'inline';
    
    hideError('eval-error');
    
    try {
        // If we already have a TTS URL, just play it
        if (appState.ttsAudioUrl) {
            ttsAudio.src = appState.ttsAudioUrl;
            ttsAudio.style.display = 'block';
            ttsAudio.play();
            return;
        }
        
        // Otherwise, generate new TTS
        const formData = new FormData();
        formData.append('text', appState.generatedText);
        if (appState.evaluationId) {
            formData.append('evaluation_id', appState.evaluationId.toString());
        }
        
        const response = await fetch('/api/generate-speech', {
            method: 'POST',
            body: formData
        });
        
        const data = await response.json();
        
        if (!response.ok) {
            throw new Error(data.error || 'فشل في توليد الصوت');
        }
        
        // Store the TTS URL
        appState.ttsAudioUrl = data.audio_url;
        
        // Set the audio source and play
        ttsAudio.src = data.audio_url;
        ttsAudio.style.display = 'block';
        ttsAudio.play();
        
    } catch (error) {
        showError('eval-error', error.message);
        console.error('Error generating TTS:', error);
        
    } finally {
        // UI: Restore button state
        playBtn.disabled = false;
        btnText.style.display = 'inline';
        btnLoading.style.display = 'none';
    }
}

