/**
 * appState - Central state object for the application
 * This keeps track of data across all three views
 */
const appState = {
    currentView: 1,           // Current visible view (1, 2, or 3)
    generatedText: '',        // The Arabic text generated by Nuha LLM
    transcribedText: '',      // The text transcribed from user's audio
    audioBlob: null,          // The recorded audio as a Blob object
    audioUrl: null,           // URL for the recorded audio (for playback)
    ttsAudioUrl: null,        // URL for the generated TTS audio
};

