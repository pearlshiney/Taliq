/**
 * handleGenerateText(event)
 * 
 * Handles the form submission for text generation.
 * Sends difficulty and length to the backend, receives generated Arabic text.
 * 
 * @param {Event} event - The form submit event
 */
async function handleGenerateText(event) {
    // Prevent default form submission (page reload)
    event.preventDefault();
    
    // Get form values
    const difficulty = document.getElementById('difficulty').value;
    const length = document.getElementById('length').value;
    
    // UI: Show loading state on button
    const generateBtn = document.getElementById('generate-btn');
    const btnText = generateBtn.querySelector('.btn-text');
    const btnLoading = generateBtn.querySelector('.btn-loading');
    
    generateBtn.disabled = true;
    btnText.style.display = 'none';
    btnLoading.style.display = 'inline';
    
    // Hide any previous errors
    hideError('generate-error');
    
    try {
        // Call the backend API to generate text
        // We use FormData because the API expects form-encoded data
        const formData = new FormData();
        formData.append('difficulty', difficulty);
        formData.append('length', length);
        
        const response = await fetch('/api/generate-text', {
            method: 'POST',
            body: formData
        });
        
        // Parse the JSON response
        const data = await response.json();
        
        if (!response.ok) {
            // Handle API error
            throw new Error(data.error || 'فشل في توليد النص');
        }
        
        // Store the generated text in state
        appState.generatedText = data.text;
        
        // Display the generated text
        const textDisplay = document.getElementById('generated-text');
        textDisplay.textContent = data.text;
        
        // Show the text display container
        document.getElementById('generated-text-container').style.display = 'block';
        
    } catch (error) {
        // Show error message to user
        showError('generate-error', error.message);
        console.error('Error generating text:', error);
        
    } finally {
        // UI: Restore button state
        generateBtn.disabled = false;
        btnText.style.display = 'inline';
        btnLoading.style.display = 'none';
    }
}

/**
 * goToRecordView()
 * 
 * Transitions from View 1 to View 2.
 * Copies the generated text to View 2's display area.
 */
function goToRecordView() {
    // Copy the generated text to View 2
    const originalTextDisplay = document.getElementById('original-text-display');
    originalTextDisplay.textContent = appState.generatedText;
    
    // Switch to View 2
    showView(2);
}

